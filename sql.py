def pullt(require, info):
    if not info in argv:
        print(f"Netkit: {require} requires {info}.")
        exit()
    try:
        typ = argv.index(info)+1
        typ = argv[typ]
    except:
        print("Netkit: missing arguments. Type -h to see the list of commands. ")
        exit()

    if "--dbs" in argv:
        print("Netkit: Args invalids")
        exit()
    return typ

